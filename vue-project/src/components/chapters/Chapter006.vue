    <div>
        Ojo had never traveled before and so he only knew that the path down the mountainside led into the open Munchkin
        Country, where large numbers of people dwelt. Scraps was quite new and not supposed to know anything of the Land
        of Oz, while the Glass Cat admitted she had never wandered very far away from the Magician's house. There was
        only one path before them, at the beginning, so they could not miss their way, and for a time they walked
        through the thick forest in silent thought, each one impressed with the importance of the adventure they had
        undertaken.
    </div>
    <div>
        Suddenly the Patchwork Girl laughed. It was funny to see her laugh, because her cheeks wrinkled up, her nose
        tipped, her silver button eyes twinkled and her mouth curled at the corners in a comical way.
    </div>
    <div>
        "Has something pleased you?" asked Ojo, who was feeling solemn and joyless through thinking upon his uncle's sad
        fate.
    </div>
    <div>
        "Yes," she answered. "Your world pleases me, for it's a queer world, and life in it is queerer still. Here am I,
        made from an old bedquilt and intended to be a slave to Margolotte, rendered free as air by an accident that
        none of you could foresee. I am enjoying life and seeing the world, while the woman who made me is standing
        helpless as a block of wood. If that isn't funny enough to laugh at, I don't know what is."
    </div>
    <div>
        "You're not seeing much of the world yet, my poor, innocent Scraps," remarked the Cat. "The world doesn't
        consist wholly of the trees that are on all sides of us."
    </div>
    <div>
        "But they're part of it; and aren't they pretty trees?" returned Scraps, bobbing her head until her brown yarn
        curls fluttered in the breeze. "Growing between them I can see lovely ferns and wild-flowers, and soft green
        mosses. If the rest of your world is half as beautiful I shall be glad I'm alive."
    </div>
    <div>
        "I don't know what the rest of the world is like, I'm sure," said the cat; "but I mean to find out."
    </div>
    <div>
        "I have never been out of the forest," Ojo added; "but to me the trees are gloomy and sad and the wild-flowers
        seem lonesome. It must be nicer where there are no trees and there is room for lots of people to live together."
    </div>
    <div>
        "I wonder if any of the people we shall meet will be as splendid as I am," said the Patchwork Girl. "All I have
        seen, so far, have pale, colorless skins and clothes as blue as the country they live in, while I am of many
        gorgeous colors—face and body and clothes. That is why I am bright and contented, Ojo, while you are blue and
        sad."
    </div>
    <div>
        "I think I made a mistake in giving you so many sorts of brains," observed the boy. "Perhaps, as the Magician
        said, you have an overdose, and they may not agree with you."
    </div>
    <div>
        "What had you to do with my brains?" asked Scraps.
    </div>
    <div>
        "A lot," replied Ojo. "Old Margolotte meant to give you only a few—just enough to keep you going—but when she
        wasn't looking I added a good many more, of the best kinds I could find in the Magician's cupboard."
    </div>
    <div>
        "Thanks," said the girl, dancing along the path ahead of Ojo and then dancing back to his side. "If a few brains
        are good, many brains must be better."
    </div>
    <div>
        "But they ought to be evenly balanced," said the boy, "and I had no time to be careful. From the way you're
        acting, I guess the dose was badly mixed."
    </div>
    <div>
        "Scraps hasn't enough brains to hurt her, so don't worry," remarked the cat, which was trotting along in a very
        dainty and graceful manner. "The only brains worth considering are mine, which are pink. You can see 'em work."
    </div>
    <div>
        After walking a long time they came to a little brook that trickled across the path, and here Ojo sat down to
        rest and eat something from his basket. He found that the Magician had given him part of a loaf of bread and a
        slice of cheese. He broke off some of the bread and was surprised to find the loaf just as large as it was
        before. It was the same way with the cheese: however much he broke off from the slice, it remained exactly the
        same size.
    </div>
    <div>
        "Ah," said he, nodding wisely; "that's magic. Dr. Pipt has enchanted the bread and the cheese, so it will last
        me all through my journey, however much I eat."
    </div>
    <div>
        "Why do you put those things into your mouth?" asked Scraps, gazing at him in astonishment. "Do you need more
        stuffing? Then why don't you use cotton, such as I am stuffed with?"
    </div>
    <div>
        "I don't need that kind," said Ojo.
    </div>
    <div>
        "But a mouth is to talk with, isn't it?"
    </div>
    <div>
        "It is also to eat with," replied the boy. "If I didn't put food into my mouth, and eat it, I would get hungry
        and starve.
    </div>
    <div>
        "Ah, I didn't know that," she said. "Give me some."
    </div>
    <div>
        Ojo handed her a bit of the bread and she put it in her mouth.
    </div>
    <div>
        "What next?" she asked, scarcely able to speak.
    </div>
    <div>
        "Chew it and swallow it," said the boy.
    </div>
    <div>
        Scraps tried that. Her pearl teeth were unable to chew the bread and beyond her mouth there was no opening.
        Being unable to swallow she threw away the bread and laughed.
    </div>
    <div>
        "I must get hungry and starve, for I can't eat," she said.
    </div>
    <div>
        "Neither can I," announced the cat; "but I'm not fool enough to try. Can't you understand that you and I are
        superior people and not made like these poor humans?"
    </div>
    <div>
        "Why should I understand that, or anything else?" asked the girl. "Don't bother my head by asking conundrums, I
        beg of you. Just let me discover myself in my own way."
    </div>
    <div>
        With this she began amusing herself by leaping across the brook and back again.
    </div>
    <div>
        "Be careful, or you'll fall in the water," warned Ojo.
    </div>
    <div>
        "Never mind."
    </div>
    <div>
        "You'd better. If you get wet you'll be soggy and can't walk. Your colors might run, too," he said.
    </div>
    <div>
        "Don't my colors run whenever I run?" she asked.
    </div>
    <div>
        "Not in the way I mean. If they get wet, the reds and greens and yellows and purples of your patches might run
        into each other and become just a blur—no color at all, you know."
    </div>
    <div>
        "Then," said the Patchwork Girl, "I'll be careful, for if I spoiled my splendid colors I would cease to be
        beautiful."
    </div>
    <div>
        "Pah!" sneered the Glass Cat, "such colors are not beautiful; they're ugly, and in bad taste. Please notice that
        my body has no color at all. I'm transparent, except for my exquisite red heart and my lovely pink brains—you
        can see 'em work."
    </div>
    <div>
        "Shoo—shoo—shoo!" cried Scraps, dancing around and laughing. "And your horrid green eyes, Miss Bungle! You can't
        see your eyes, but we can, and I notice you're very proud of what little color you have. Shoo, Miss Bungle,
        shoo—shoo—shoo! If you were all colors and many colors, as I am, you'd be too stuck up for anything." She leaped
        over the cat and back again, and the startled Bungle crept close to a tree to escape her. This made Scraps laugh
        more heartily than ever, and she said:
    </div>
    <div>
        <div>
            "Whoop-te-doodle-doo!
        </div>
        <div>
            The cat has lost her shoe.
        </div>
        <div>
            Her tootsie's bare, but she don't care,
        </div>
        <div>
            So what's the odds to you?"
        </div>
    </div>
    <div>
        "Dear me, Ojo," said the cat; "don't you think the creature is a little bit crazy?"
    </div>
    <div>
        "It may be," he answered, with a puzzled look.
    </div>
    <div>
        "If she continues her insults I'll scratch off her suspender-button eyes," declared the cat.
    </div>
    <div>
        "Don't quarrel, please," pleaded the boy, rising to resume the journey. "Let us be good comrades and as happy
        and cheerful as possible, for we are likely to meet with plenty of trouble on our way."
    </div>
    <div>
        It was nearly sundown when they came to the edge of the forest and saw spread out before them a delightful
        landscape. There were broad blue fields stretching for miles over the valley, which was dotted everywhere with
        pretty, blue domed houses, none of which, however, was very near to the place where they stood. Just at the
        point where the path left the forest stood a tiny house covered with leaves from the trees, and before this
        stood a Munchkin man with an axe in his hand. He seemed very much surprised when Ojo and Scraps and the Glass
        Cat came out of the woods, but as the Patchwork Girl approached nearer he sat down upon a bench and laughed so
        hard that he could not speak for a long time.
    </div>
    <div>
        This man was a woodchopper and lived all alone in the little house. He had bushy blue whiskers and merry blue
        eyes and his blue clothes were quite old and worn.
    </div>
    <div>
        "Mercy me!" exclaimed the woodchopper, when at last he could stop laughing. "Who would think such a funny
        harlequin lived in the Land of Oz? Where did you come from, Crazy-quilt?"
    </div>
    <div>
        "Do you mean me?" asked the Patchwork Girl.
    </div>
    <div>
        "Of course," he replied.
    </div>
    <div>
        "You misjudge my ancestry. I'm not a crazy-quilt; I'm patchwork," she said.
    </div>
    <div>
        "There's no difference," he replied, beginning to laugh again. "When my old grandmother sews such things
        together she calls it a crazy-quilt; but I never thought such a jumble could come to life."
    </div>
    <div>
        "It was the Magic Powder that did it," explained Ojo.
    </div>
    <div>
        "Oh, then you have come from the Crooked Magician on the mountain. I might have known it, for—Well, I declare!
        here's a glass cat. But the Magician will get in trouble for this; it's against the law for anyone to work magic
        except Glinda the Good and the royal Wizard of Oz. If you people—or things—or glass spectacles—or
        crazy-quilts—or whatever you are, go near the Emerald City, you'll be arrested."
    </div>
    <div>
        "We're going there, anyhow," declared Scraps, sitting upon the bench and swinging her stuffed legs.
    </div>
    <div>
        <div>
            "If any of us takes a rest,
        </div>
        <div>
            We'll be arrested sure,
        </div>
        <div>
            And get no restitution
        </div>
        <div>
            'Cause the rest we must endure."
        </div>
    </div>
    <div>
        "I see," said the woodchopper, nodding; "you're as crazy as the crazy-quilt you're made of."
    </div>
    <div>
        "She really is crazy," remarked the Glass Cat. "But that isn't to be wondered at when you remember how many
        different things she's made of. For my part, I'm made of pure glass—except my jewel heart and my pretty pink
        brains. Did you notice my brains, stranger? You can see 'em work."
    </div>
    <div>
        "So I can," replied the woodchopper; "but I can't see that they accomplish much. A glass cat is a useless sort
        of thing, but a Patchwork Girl is really useful. She makes me laugh, and laughter is the best thing in life.
        There was once a woodchopper, a friend of mine, who was made all of tin, and I used to laugh every time I saw
        him."
    </div>
    <div>
        "A tin woodchopper?" said Ojo. "That is strange."
    </div>
    <div>
        "My friend wasn't always tin," said the man, "but he was careless with his axe, and used to chop himself very
        badly. Whenever he lost an arm or a leg he had it replaced with tin; so after a while he was all tin."
    </div>
    <div>
        "And could he chop wood then?" asked the boy.
    </div>
    <div>
        "He could if he didn't rust his tin joints. But one day he met Dorothy in the forest and went with her to the
        Emerald City, where he made his fortune. He is now one of the favorites of Princess Ozma, and she has made him
        the Emperor of the Winkies—the Country where all is yellow."
    </div>
    <div>
        "Who is Dorothy?" inquired the Patchwork Girl.
    </div>
    <div>
        "A little maid who used to live in Kansas, but is now a Princess of Oz. She's Ozma's best friend, they say, and
        lives with her in the royal palace."
    </div>
    <div>
        "Is Dorothy made of tin?" inquired Ojo.
    </div>
    <div>
        "Is she patchwork, like me?" inquired Scraps.
    </div>
    <div>
        "No," said the man; "Dorothy is flesh, just as I am. I know of only one tin person, and that is Nick Chopper,
        the Tin Woodman; and there will never be but one Patchwork Girl, for any magician that sees you will refuse to
        make another one like you."
    </div>
    <div>
        "I suppose we shall see the Tin Woodman, for we are going to the Country of the Winkies," said the boy.
    </div>
    <div>
        "What for?" asked the woodchopper.
    </div>
    <div>
        "To get the left wing of a yellow butterfly."
    </div>
    <div>
        "It is a long journey," declared the man, "and you will go through lonely parts of Oz and cross rivers and
        traverse dark forests before you get there."
    </div>
    <div>
        "Suits me all right," said Scraps. "I'll get a chance to see the country."
    </div>
    <div>
        "You're crazy, girl. Better crawl into a rag-bag and hide there; or give yourself to some little girl to play
        with. Those who travel are likely to meet trouble; that's why I stay at home."
    </div>
    <div>
        The woodchopper then invited them all to stay the night at his little hut, but they were anxious to get on and
        so left him and continued along the path, which was broader, now, and more distinct.
    </div>
    <div>
        They expected to reach some other house before it grew dark, but the twilight was brief and Ojo soon began to
        fear they had made a mistake in leaving the woodchopper.
    </div>
    <div>
        "I can scarcely see the path," he said at last. "Can you see it, Scraps?"
    </div>
    <div>
        "No," replied the Patchwork Girl, who was holding fast to the boy's arm so he could guide her.
    </div>
    <div>
        "I can see," declared the Glass Cat. "My eyes are better than yours, and my pink brains—"
    </div>
    <div>
        "Never mind your pink brains, please," said Ojo hastily; "just run ahead and show us the way. Wait a minute and
        I'll tie a string to you; for then you can lead us."
    </div>
    <div>
        He got a string from his pocket and tied it around the cat's neck, and after that the creature guided them along
        the path. They had proceeded in this way for about an hour when a twinkling blue light appeared ahead of them.
    </div>
    <div>
        "Good! there's a house at last," cried Ojo. "When we reach it the good people will surely welcome us and give us
        a night's lodging." But however far they walked the light seemed to get no nearer, so by and by the cat stopped
        short, saying:
    </div>
    <div>
        "I think the light is traveling, too, and we shall never be able to catch up with it. But here is a house by the
        roadside, so why go farther?"
    </div>
    <div>
        "Where is the house, Bungle?"
    </div>
    <div>
        "Just here beside us, Scraps."
    </div>
    <div>
        Ojo was now able to see a small house near the pathway. It was dark and silent, but the boy was tired and wanted
        to rest, so he went up to the door and knocked.
    </div>
    <div>
        "Who is there?" cried a voice from within.
    </div>
    <div>
        "I am Ojo the Unlucky, and with me are Miss Scraps Patchwork and the Glass Cat," he replied.
    </div>
    <div>
        "What do you want?" asked the Voice.
    </div>
    <div>
        "A place to sleep," said Ojo.
    </div>
    <div>
        "Come in, then; but don't make any noise, and you must go directly to bed," returned the Voice.
    </div>
    <div>
        Ojo unlatched the door and entered. It was very dark inside and he could see nothing at all. But the cat
        exclaimed: "Why, there's no one here!"
    </div>
    <div>
        "There must be," said the boy. "Some one spoke to me."
    </div>
    <div>
        "I can see everything in the room," replied the cat, "and no one is present but ourselves. But here are three
        beds, all made up, so we may as well go to sleep."
    </div>
    <div>
        "What is sleep?" inquired the Patchwork Girl.
    </div>
    <div>
        "It's what you do when you go to bed," said Ojo.
    </div>
    <div>
        "But why do you go to bed?" persisted the Patchwork Girl.
    </div>
    <div>
        "Here, here! You are making altogether too much noise," cried the Voice they had heard before. "Keep quiet,
        strangers, and go to bed."
    </div>
    <div>
        The cat, which could see in the dark, looked sharply around for the owner of the Voice, but could discover no
        one, although the Voice had seemed close beside them. She arched her back a little and seemed afraid. Then she
        whispered to Ojo: "Come!" and led him to a bed.
    </div>
    <div>
        With his hands the boy felt of the bed and found it was big and soft, with feather pillows and plenty of
        blankets. So he took off his shoes and hat and crept into the bed. Then the cat led Scraps to another bed and
        the Patchwork Girl was puzzled to know what to do with it.
    </div>
    <div>
        "Lie down and keep quiet," whispered the cat, warningly.
    </div>
    <div>
        "Can't I sing?" asked Scraps.
    </div>
    <div>
        "No."
    </div>
    <div>
        "Can't I whistle?" asked Scraps.
    </div>
    <div>
        "No."
    </div>
    <div>
        "Can't I dance till morning, if I want to?" asked Scraps.
    </div>
    <div>
        "You must keep quiet," said the cat, in a soft voice.
    </div>
    <div>
        "I don't want to," replied the Patchwork Girl, speaking as loudly as usual. "What right have you to order me
        around? If I want to talk, or yell, or whistle—"
    </div>
    <div>
        Before she could say anything more an unseen hand seized her firmly and threw her out of the door, which closed
        behind her with a sharp slam. She found herself bumping and rolling in the road and when she got up and tried to
        open the door of the house again she found it locked.
    </div>
    <div>
        "What has happened to Scraps?" asked Ojo.
    </div>
    <div>
        "Never mind. Let's go to sleep, or something will happen to us," answered the Glass Cat.
    </div>
    <div>
        So Ojo snuggled down in his bed and fell asleep, and he was so tired that he never wakened until broad daylight.
