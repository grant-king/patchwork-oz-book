</div>
<div>
"Why?" asked the Patchwork Girl.
</div>
<div>
"Because you are so gay and gaudy."
</div>
<div>
"It is my beauty that dazzles you," she asserted. "You Munchkin people all strut around in your stupid blue color, while I—"
